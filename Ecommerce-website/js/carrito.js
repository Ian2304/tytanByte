function actualizarTotal() {
    const totalCalculado = productosEnCarrito.reduce((acc, producto) => acc + (Number(producto.precio) * producto.cantidad), 0);
    const totalFormateado = new Intl.NumberFormat('es-CO', { style: 'currency', currency: 'COP' }).format(totalCalculado);
    total.innerText= totalFormateado;
    
}





